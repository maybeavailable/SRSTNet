import torch
import torch.nn as nn
from einops import rearrange

class HFA(nn.Module):
    def __init__(self, dim, head_num, window_size=7, group_kernel_sizes=[3, 5, 7, 9], 
                 qkv_bias=False, fuse_bn=False, down_sample_mode='avg_pool', 
                 attn_drop_ratio=0., gate_layer='sigmoid'):
        super(HFA, self).__init__()

        self.dim = dim
        self.head_num = head_num
        self.head_dim = dim // head_num
        self.scaler = self.head_dim ** -0.5
        self.group_kernel_sizes = group_kernel_sizes
        self.window_size = window_size
        self.qkv_bias = qkv_bias
        self.fuse_bn = fuse_bn
        self.down_sample_mode = down_sample_mode

        assert dim % 4 == 0, "Input dimension must be divisible by 4."
        self.group_chans = dim // 4

        self.local_dwc = nn.Conv1d(self.group_chans, self.group_chans, 
                                   kernel_size=group_kernel_sizes[0], 
                                   padding=group_kernel_sizes[0] // 2, 
                                   groups=self.group_chans)
        self.global_dwc_s = nn.Conv1d(self.group_chans, self.group_chans, 
                                      kernel_size=group_kernel_sizes[1], 
                                      padding=group_kernel_sizes[1] // 2, 
                                      groups=self.group_chans)
        self.global_dwc_m = nn.Conv1d(self.group_chans, self.group_chans, 
                                      kernel_size=group_kernel_sizes[2], 
                                      padding=group_kernel_sizes[2] // 2, 
                                      groups=self.group_chans)
        self.global_dwc_l = nn.Conv1d(self.group_chans, self.group_chans, 
                                      kernel_size=group_kernel_sizes[3], 
                                      padding=group_kernel_sizes[3] // 2, 
                                      groups=self.group_chans)

        self.sa_gate = nn.Softmax(dim=2) if gate_layer == 'softmax' else nn.Sigmoid()
        self.norm_h = nn.GroupNorm(4, dim)
        self.norm_w = nn.GroupNorm(4, dim)
        self.conv_d = nn.Identity()
        self.norm = nn.GroupNorm(1, dim)

        self.q = nn.Conv2d(dim, dim, kernel_size=1, bias=qkv_bias, groups=dim)
        self.k = nn.Conv2d(dim, dim, kernel_size=1, bias=qkv_bias, groups=dim)
        self.v = nn.Conv2d(dim, dim, kernel_size=1, bias=qkv_bias, groups=dim)
        self.aggten = nn.Conv2d(dim, dim, kernel_size=3, padding=1, bias=qkv_bias, groups=dim)
        self.attn_drop = nn.Dropout(attn_drop_ratio)
        self.ca_gate = nn.Softmax(dim=1) if gate_layer == 'softmax' else nn.Sigmoid()
        self.cast = nn.Conv1d(dim, dim, kernel_size=5, stride=1, padding=2, groups=dim)

        if window_size == -1:
            self.down_func = nn.AdaptiveAvgPool2d((1, 1))
        elif down_sample_mode == 'recombination':
            self.down_func = self.space_to_chans
            self.conv_d = nn.Conv2d(dim * window_size ** 2, dim, kernel_size=1, bias=False)
        elif down_sample_mode == 'avg_pool':
            self.down_func = nn.AvgPool2d(kernel_size=window_size, stride=window_size)
        elif down_sample_mode == 'max_pool':
            self.down_func = nn.MaxPool2d(kernel_size=window_size, stride=window_size)

    def forward(self, x):
        b, c, h_, w_ = x.size()
        x_h = x.mean(dim=3)
        l_x_h, g_x_h_s, g_x_h_m, g_x_h_l = torch.split(x_h, self.group_chans, dim=1)
        x_w = x.mean(dim=2)
        l_x_w, g_x_w_s, g_x_w_m, g_x_w_l = torch.split(x_w, self.group_chans, dim=1)

        x_h_attn = self.sa_gate(self.norm_h(torch.cat([
            self.local_dwc(l_x_h),
            self.global_dwc_s(g_x_h_s),
            self.global_dwc_m(g_x_h_m),
            self.global_dwc_l(g_x_h_l)], dim=1) * self.cast(x_h)))
        x_h_attn = x_h_attn.view(b, c, h_, 1)

        x_w_attn = self.sa_gate(self.norm_w(torch.cat([
            self.local_dwc(l_x_w),
            self.global_dwc_s(g_x_w_s),
            self.global_dwc_m(g_x_w_m),
            self.global_dwc_l(g_x_w_l)], dim=1) * self.cast(x_w)))
        x_w_attn = x_w_attn.view(b, c, 1, w_)

        x = x * x_h_attn * x_w_attn
        y = self.down_func(x)
        y = self.conv_d(y)

        _, _, h_, w_ = y.size()
        y = self.norm(y)
        q = self.q(y)
        k = self.k(y)
        v = self.v(y)
        aggten = self.aggten(q)

        q = rearrange(q, 'b (head_num head_dim) h w -> b head_num head_dim (h w)', 
                      head_num=self.head_num, head_dim=self.head_dim)
        k = rearrange(k, 'b (head_num head_dim) h w -> b head_num head_dim (h w)', 
                      head_num=self.head_num, head_dim=self.head_dim)
        v = rearrange(v, 'b (head_num head_dim) h w -> b head_num head_dim (h w)', 
                      head_num=self.head_num, head_dim=self.head_dim)
        aggten = rearrange(aggten, 'b (head_num head_dim) h w -> b head_num head_dim (h w)', 
                           head_num=self.head_num, head_dim=self.head_dim)

        attn = (aggten @ k.transpose(-2, -1)) * self.scaler
        attn = self.attn_drop(attn.softmax(dim=-1))
        attn = attn @ v

        attn2 = (q @ aggten.transpose(-2, -1)) * self.scaler
        attn2 = self.attn_drop(attn2.softmax(dim=-1))
        attn = attn2 @ attn

        attn = rearrange(attn, 'b head_num head_dim (h w) -> b (head_num head_dim) h w', 
                         h=h_, w=w_)
        attn = attn.mean((2, 3), keepdim=True)
        attn = self.ca_gate(attn)

        return attn * x


class RFE(nn.Module):
    def __init__(self, n_feats):
        super().__init__()
        self.n_feats = n_feats
        self.i_feats = 2 * n_feats

        
        self.norm = LayerNorm(n_feats, data_format='channels_first')
        self.scale = nn.Parameter(torch.zeros((1, n_feats, 1, 1)), requires_grad=True)

        k = 2
        self.LKA7 = self._create_lka_block(n_feats // k, [7, 9], [1, 4])
        self.LKA5 = self._create_lka_block(n_feats // k, [5, 7], [1, 3])
        self.LKA3 = self._create_lka_block(n_feats // k, [3, 5], [1, 2])
        self.X3 = nn.Conv2d(n_feats // k, n_feats // k, 3, 1, 1, groups=n_feats // k)
        self.X5 = nn.Conv2d(n_feats // k, n_feats // k, 5, 1, 5 // 2, groups=n_feats // k)
        self.X7 = nn.Conv2d(n_feats // k, n_feats // k, 7, 1, 7 // 2, groups=n_feats // k)

        self.proj_first = nn.Conv2d(n_feats, self.i_feats, 1, 1, 0)
        self.proj_last = nn.Conv2d(n_feats, n_feats, 1, 1, 0)

        self.Conv1 = nn.Conv2d(n_feats, self.i_feats, 1, 1, 0)
        self.DWConv1 = nn.Conv2d(n_feats, n_feats, 7, 1, 7 // 2, groups=n_feats)
        self.Conv2 = nn.Conv2d(n_feats, n_feats, 1, 1, 0)

    def _create_lka_block(self, channels, kernel_sizes, dilations):
        return nn.Sequential(
            nn.Conv2d(channels, channels, kernel_sizes[0], 1, kernel_sizes[0] // 2, groups=channels),
            nn.Conv2d(channels, channels, kernel_sizes[1], 1, (kernel_sizes[1] // 2) * dilations[1], groups=channels, dilation=dilations[1]),
            nn.Conv2d(channels, channels, 1, 1, 0)
        )

    def forward(self, x):
        shortcut = x.clone()
        x = self.norm(x)

        x = self.proj_first(x)
        a, x = torch.chunk(x, 2, dim=1)
        a_1, a_2 = torch.chunk(a, 2, dim=1)
        a = torch.cat([
            self.LKA7(a_1) * self.X7(a_1),
            self.LKA5(a_2) * self.X5(a_2)
        ], dim=1)
        x = self.proj_last(x * a) * self.scale + shortcut

        shortcut = x.clone()
        x = self.Conv1(self.norm(x))
        a, x = torch.chunk(x, 2, dim=1)
        x = x * self.DWConv1(a)
        x = self.Conv2(x)

        return x * self.scale + shortcut